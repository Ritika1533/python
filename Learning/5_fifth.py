#WHILE LOOP 
count=1
while count<=5 :
    print("hello",count)
    count=count+1
print("loop ended")

#1 TO 5
i=1
while i<=5 : 
    print(i)
    i+=1
print("loop ended")

#5 TO 1
i=5
while i>=1 : 
    print(i)
    i-=1
print("loop ended reverse")

#FOR LOOP
list=[4,8,9,5,0,6]
for val in list :
    print(val)
else:
    print("end")